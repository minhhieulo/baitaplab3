package baitap3;

public class Point2D {
    private Float x=0.0f;
    private Float y=0.0f;

    public Point2D() {
    }

    public Point2D(Float x, Float y) {
        this.x = x;
        this.y = y;
    }
    
    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }
    
    
}
